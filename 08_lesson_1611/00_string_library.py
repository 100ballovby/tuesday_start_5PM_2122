import string as s

print(s.ascii_lowercase)  # английский алфавит (маленькие буквы)
print(s.ascii_uppercase)  # английский алфавит (большие буквы)
print(s.ascii_letters)  # весь алфавит
print(s.punctuation)  # спец.символы
print(s.digits)  # цифры
print(s.octdigits)  # восьмеричная система
print(s.hexdigits)  # 16-ричная система
print(s.printable)  # весь набор
