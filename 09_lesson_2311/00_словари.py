shop = {
    'ветчина': 5.78,
    'кефир': 2.09,
    'молоко': 3.15,
    'сыр': 9.99,
}

print(type(shop))  # <class 'dict'> dictionary - словарь

# вывод значения по ключу
print(shop['молоко'])

# добавление ПАРЫ ключ:значение
shop['виноград'] = 15.78
print(shop)

# изменять ЗНАЧЕНИЕ (сам ключ редактированию не подлежит)
shop['кефир'] = 4.56
print(shop)

# удаление ПАРЫ
del shop['ветчина']
print(shop)
